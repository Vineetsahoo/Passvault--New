import multer from 'multer';

// Memory storage instead of diskStorage — files never touch the local
// filesystem now, which matters because most hosts (Railway included)
// don't guarantee disk persistence across deploys/restarts. req.file.buffer
// goes straight to Supabase Storage in the route handler.
const storage = multer.memoryStorage();

const fileFilter = (req, file, cb) => {
  const allowedMimeTypes = [
    'application/pdf',
    'image/jpeg',
    'image/jpg',
    'image/png',
    'image/gif',
    'application/msword',
    'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
    'application/vnd.ms-excel',
    'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
  ];

  if (allowedMimeTypes.includes(file.mimetype)) {
    cb(null, true);
  } else {
    cb(new Error('Invalid file type. Only PDF, images, and Office documents are allowed.'), false);
  }
};

const upload = multer({
  storage,
  fileFilter,
  limits: {
    fileSize: 10 * 1024 * 1024, // 10MB — bump to 50MB to match documents.js's old limit if needed
  },
});

export default upload;
